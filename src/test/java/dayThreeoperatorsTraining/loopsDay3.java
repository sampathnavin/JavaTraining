package dayThreeoperatorsTraining;

public class loopsDay3 {

	public static void main(String[] args) {
		
		// Loop 
		
		// For Loop 
		// While 
		// Do while 
		
		// If else 
		
		
		int i = 1 ;
		 // +1 ;
		int o = 500 ;
		
		while(i<=o) //Condition
		{
			System.out.println("*");
			i++; //Increment or Decrement i --
		}
		
		
		// For Loop 
		
		// Initilizing // Conditioning // Increments 
		
		int s = 100 ;
		
		for(int t = 1;t<=s ; t++)
		{
			System.out.println("*");
		}
		
		
		 // 1 2 3 4 5 6 7 8 9
		
		
		int p = 9;
		
		for(int q = 1 ; q <=p ; q++)
		{
			System.out.println(q);
		}
		
		int w = 500 ;
		
		for(int z = 1; z <w ; z ++)
		{
			System.out.println(z);
		}
		
		
		

	}

}
