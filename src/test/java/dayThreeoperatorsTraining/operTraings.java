package dayThreeoperatorsTraining;

public class operTraings {

	public static void main(String[] args) {
		
		//Operators 
		// + , - , / , * ,
		// == , >= , <= , != , && , ||
		
		//Addition 
		int a = 10 ;
		int b = 10 ;
		int c = a+b;	
		System.out.println(a==b);
		System.out.println(c);
		
		//Sub
		int s = 50;
		int d = 30 ;
		int j = s-d ;
		System.out.println(s>=d);
		System.out.println("The subracted value is " + j);
		
		//Div 
		int p = 12 ;
		int v = 13 ;
		int o = p/v ;
		System.out.println(p<=v);
		System.out.println(o);
		
		//Multiplication 
		int q = 12 ;
		int e = 13 ;
		int t = q*e ;
		System.out.println(q != e);
		System.out.println(t);
		
		
		
		
	}

}
