package dayThreeoperatorsTraining;

public class loops {

	public static void main(String[] args) {
		
		
		System.out.println("*");

		
		int i = 1 ;
		
		int p = 500 ;
		
		while (i<=p)
		{
			System.out.println("*");
			i++ ;
		}
		
		for(int q =1 ;q <=p ; q ++)
		{
			System.out.println(q);
		}
		
		
		
		
	}

}
